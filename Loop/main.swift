import Foundation

//色々なfor in Loop
let japan: Array = ["Hokkaido", "Tokyo", "Osaka"]
for Japan in japan {
    print(Japan)
}

let score = ["Shogo": 90, "Akira": 80, "Satoru": 70]
for Score in score {
    print(Score.key)
    print(Score.value)
    
}

let value = 1..<5
for _ in value {
   print("4Loops")
}









